const getFriendsByOnlineStatus = function (allFriends) {
  const friendsByStatus = {
    online: [],
    offline: [],
  };

  for (const friend of allFriends) {
    if (friend.online) {
      friendsByStatus.online.push(friend);

      continue;
    }

    friendsByStatus.offline.push(friend);
  }

  return friendsByStatus;
};

